module WorkflowMgr

  ########################################## 
  #
  # Class CycleString
  #
  ##########################################
  class CycleString

    #####################################################
    #
    # initialize
    #
    #####################################################
    def initialize(str,offset)

      @str=str
      @offset=offset

    end

    #####################################################
    #
    # to_s
    #
    #####################################################
    def to_s(cycle)

      return (cycle.gmtime+@offset).strftime(@str)

    end

  end

end
