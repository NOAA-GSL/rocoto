module WorkflowMgr

  ########################################## 
  #
  # Class CycleString
  #
  ##########################################
  class CycleString

    attr :str
    attr :offset

    #####################################################
    #
    # initialize
    #
    #####################################################
    def initialize(str,offset)

      @str=str
      @offset=offset

    end

    #####################################################
    #
    # to_s
    #
    #####################################################
    def to_s(cycle)

      return (cycle.gmtime+@offset).strftime(@str)

    end


    #####################################################
    #
    # hash
    #
    #####################################################
    def hash

      @str.hash ^ @offset.hash

    end


    #####################################################
    #
    # eql?
    #
    #####################################################
    def eql?(other)

      return @str==other.str && @offset==other.offset

    end

  end

end
