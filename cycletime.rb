##########################################
#
# Class CycleTime
#
##########################################
class CycleTime
 
  #####################################################
  #
  # initialize
  #
  #####################################################
  def initialize(offset)

    @offset=offset

  end

end


##########################################
#
# Class Cycle_Y
#
##########################################
class Cycle_Y < CycleTime

  #####################################################
  #
  # to_s
  #
  #####################################################
  def to_s(cycle)

     return (cycle.gmtime+@offset).strftime("%Y")

  end

end  


